//! G2 conformance-trace emitter (PLAN.md, drift-defense gate). Reads a bake's `levels.json`
//! + `golden-lines.json` and emits, per level, a bounded trace of (state, capped-move-set,
//! transition) evidence for the JS side (`scripts/verify-trace.ts`) to replay cheaply:
//!
//!   - every state along the golden optimal line, with the full capped-successor move set
//!     Rust's searches consider there (JS asserts set-equality against its own player rules,
//!     catching drift in BOTH directions), and
//!   - K seeded random offshoots (one extra capped move from a random line state, with the
//!     resulting state + its move set) so coverage isn't limited to the optimal corridor;
//!   - plus a small seeded random corpus (fresh generated boards, all mechanics at balanced
//!     density) traced the same way — evidence beyond the committed levels.
//!
//! The trace is a SCRATCH artifact regenerated by the gate run — never committed. Bound per
//! level: golden line (≤ ~40 states) + K offshoots, well under the plan's ≤200 cap.
//!
//! Usage: trace --levels <levels.json> --golden <golden-lines.json> --out <trace.json>
//!              [--offshoots K] [--corpus N]

use serde::{Deserialize, Serialize};

use magic_color_core::generator::{generate_level, GenerateOptions, ParMode};
use magic_color_core::ice::IceTube;
use magic_color_core::mechanics::build_overlays;
use magic_color_core::progression::{balanced_density, seed_for_level, MECHANIC_SETS, SHAPES};
use magic_color_core::rng::Mulberry32;
use magic_color_core::search::{apply_capped_move, capped_move_set, Overlays};
use magic_color_core::state::{Hidden, State, Tube};
use magic_color_core::types::{NO_COLOR, PALETTE};

#[derive(Deserialize)]
struct LevelIn {
    level: usize,
    bottles: Vec<Vec<String>>,
    capacity: u8,
    hidden: Vec<Vec<bool>>,
    funnels: Vec<Option<String>>,
    ice: Vec<Vec<Option<String>>>,
}

#[derive(Deserialize)]
struct GoldenIn {
    level: usize,
    line: Option<Vec<(u8, u8)>>,
}

#[derive(Serialize)]
#[serde(rename_all = "camelCase")]
struct Step {
    cells: Vec<Vec<u8>>,
    hidden: Vec<u16>,
    /// Rust's capped-successor move set at this state (canonical order).
    moves: Vec<(u8, u8)>,
    /// The golden-line move taken from this state (absent on the final state).
    #[serde(skip_serializing_if = "Option::is_none")]
    apply: Option<(u8, u8)>,
}

#[derive(Serialize)]
#[serde(rename_all = "camelCase")]
struct Offshoot {
    from_step: usize,
    mv: (u8, u8),
    cells: Vec<Vec<u8>>,
    hidden: Vec<u16>,
    moves: Vec<(u8, u8)>,
}

#[derive(Serialize)]
#[serde(rename_all = "camelCase")]
struct TraceOut {
    id: String,
    capacity: u8,
    /// Palette indices; 255 = none. Shapes match the baked JSON (funnels per tube, ice per cell).
    funnels: Vec<Option<u8>>,
    ice: Vec<Vec<Option<u8>>>,
    steps: Vec<Step>,
    offshoots: Vec<Offshoot>,
}

fn color_idx(name: &str) -> u8 {
    PALETTE.iter().position(|p| *p == name).expect("known palette id") as u8
}

fn cells_of(state: &State) -> Vec<Vec<u8>> {
    state.tubes.iter().map(|t| t.cells().to_vec()).collect()
}

fn trace_board(
    id: String,
    state0: State,
    hidden0: Hidden,
    funnels: Vec<u8>,
    ice: Vec<IceTube>,
    line: &[(u8, u8)],
    offshoot_count: usize,
    offshoot_seed: u32,
) -> TraceOut {
    let overlays = Overlays { funnels: Some(&funnels), ice: Some(&ice) };

    // Walk the golden line, recording each state and its move set.
    let mut steps: Vec<Step> = Vec::new();
    let mut states: Vec<(State, Hidden)> = vec![(state0.clone(), hidden0.clone())];
    let mut cur = state0.clone();
    let mut hid = hidden0.clone();
    for (i, &mv) in line.iter().enumerate() {
        steps.push(Step {
            cells: cells_of(&cur),
            hidden: hid.clone(),
            moves: capped_move_set(&cur, &hid, overlays),
            apply: Some(mv),
        });
        let (next, next_hid) =
            apply_capped_move(&cur, &hid, overlays, mv).unwrap_or_else(|| panic!("{id}: golden move {i} not in capped set"));
        cur = next;
        hid = next_hid;
        states.push((cur.clone(), hid.clone()));
    }
    steps.push(Step { cells: cells_of(&cur), hidden: hid.clone(), moves: capped_move_set(&cur, &hid, overlays), apply: None });

    // Seeded offshoots: one extra capped move from a random line state.
    let mut rng = Mulberry32::new(offshoot_seed);
    let mut offshoots: Vec<Offshoot> = Vec::new();
    for _ in 0..offshoot_count {
        let si = (rng.next_f64() * states.len() as f64).floor() as usize;
        let (s, h) = &states[si];
        let moves = capped_move_set(s, h, overlays);
        if moves.is_empty() {
            continue;
        }
        let mv = moves[(rng.next_f64() * moves.len() as f64).floor() as usize];
        let (next, next_hid) = apply_capped_move(s, h, overlays, mv).expect("move from own set");
        let next_moves = capped_move_set(&next, &next_hid, overlays);
        offshoots.push(Offshoot { from_step: si, mv, cells: cells_of(&next), hidden: next_hid, moves: next_moves });
    }

    let ice_cells: Vec<Vec<Option<u8>>> = state0
        .tubes
        .iter()
        .enumerate()
        .map(|(b, t)| {
            let it = ice[b];
            (0..t.len())
                .map(|i| {
                    if it.trigger != NO_COLOR && i < it.height as usize { Some(it.trigger) } else { None }
                })
                .collect()
        })
        .collect();

    TraceOut {
        id,
        capacity: state0.capacity,
        funnels: funnels.iter().map(|&f| if f == NO_COLOR { None } else { Some(f) }).collect(),
        ice: ice_cells,
        steps,
        offshoots,
    }
}

fn main() {
    let args: Vec<String> = std::env::args().skip(1).collect();
    let get = |flag: &str| -> Option<String> {
        args.iter().position(|a| a == flag).and_then(|i| args.get(i + 1).cloned())
    };
    let levels_path = get("--levels").expect("--levels <levels.json>");
    let golden_path = get("--golden").expect("--golden <golden-lines.json>");
    let out_path = get("--out").expect("--out <trace.json>");
    let offshoots: usize = get("--offshoots").map_or(8, |s| s.parse().unwrap());
    let corpus: usize = get("--corpus").map_or(2, |s| s.parse().unwrap());

    let levels: Vec<LevelIn> =
        serde_json::from_str(&std::fs::read_to_string(&levels_path).expect("read levels")).unwrap();
    let golden: Vec<GoldenIn> =
        serde_json::from_str(&std::fs::read_to_string(&golden_path).expect("read golden")).unwrap();
    let golden_by_level: std::collections::HashMap<usize, Option<Vec<(u8, u8)>>> =
        golden.into_iter().map(|g| (g.level, g.line)).collect();

    let mut traces: Vec<TraceOut> = Vec::new();

    // Committed-board traces (root-only when the level has no golden line).
    for l in &levels {
        let state = State {
            tubes: l
                .bottles
                .iter()
                .map(|col| Tube::from_cells(&col.iter().map(|c| color_idx(c)).collect::<Vec<_>>()))
                .collect(),
            capacity: l.capacity,
        };
        let hidden: Hidden = l
            .hidden
            .iter()
            .map(|col| col.iter().enumerate().fold(0u16, |acc, (i, &h)| if h { acc | (1 << i) } else { acc }))
            .collect();
        let funnels: Vec<u8> = l.funnels.iter().map(|f| f.as_deref().map_or(NO_COLOR, color_idx)).collect();
        let ice: Vec<IceTube> = l
            .ice
            .iter()
            .map(|col| {
                let height = col.iter().rposition(|c| c.is_some()).map_or(0, |i| i + 1);
                if height == 0 {
                    IceTube::NONE
                } else {
                    IceTube { trigger: color_idx(col[0].as_deref().unwrap()), height: height as u8 }
                }
            })
            .collect();

        let line = golden_by_level.get(&l.level).cloned().flatten().unwrap_or_default();
        traces.push(trace_board(
            format!("L{}", l.level),
            state,
            hidden,
            funnels,
            ice,
            &line,
            offshoots,
            seed_for_level(70_000 + l.level, 0),
        ));
    }

    // Seeded random corpus: fresh boards per shape, all mechanics at balanced density.
    let mechanics = MECHANIC_SETS[MECHANIC_SETS.len() - 1];
    for (si, shape) in SHAPES.iter().enumerate() {
        for k in 0..corpus {
            let seed = seed_for_level(90_000 + si * 10 + k, 0);
            let level = generate_level(&GenerateOptions {
                colors: shape.colors,
                bottles: shape.bottles,
                capacity: shape.capacity,
                seed,
                min_par: None,
                par_mode: ParMode::Proxy,
            })
            .expect("corpus generation");
            let overlays = build_overlays(mechanics, &level.state, &level.solution, seed, balanced_density());
            // Corpus boards have no golden line — trace the root + offshoots only.
            traces.push(trace_board(
                format!("corpus:{si}:{k}"),
                level.state.clone(),
                overlays.hidden.clone(),
                overlays.funnels.clone(),
                overlays.ice.clone(),
                &[],
                offshoots,
                seed ^ 0x51ed270b,
            ));
        }
    }

    let steps: usize = traces.iter().map(|t| t.steps.len()).sum();
    let shoots: usize = traces.iter().map(|t| t.offshoots.len()).sum();
    std::fs::write(&out_path, serde_json::to_string(&traces).unwrap()).expect("write trace");
    println!("wrote {out_path}: {} traces, {steps} line states, {shoots} offshoots", traces.len());
}
